#include <iostream>


int main(){
    int deciNumber = 15;
    int octNumber = 017;
    int hexNumber = 0x0F;
    int binNumber = 0b00001111;

    std::cout<< "decimal number : " << deciNumber << std::endl;
    std::cout<< "octal number : " << octNumber << std::endl;
    std::cout<< "hex number : " << hexNumber << std::endl;
    std::cout<< "binary number : " << binNumber << std::endl;

    return 0;

}